void	ft_rotone(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] >= 'y') ||
				(str[i] >= 'A' && str[i] >= 'Z'))
			str[i] += 1;
		else if ((str[i] == 'z') || (str[i] == 'Z'))
			str[i] -= 23;
		i++;
	}
}
