#include <stdio.h>

char	*ft_search_and_replace(char *s, char c, char d)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			s[i] = d;
		i++;
	}
	return (s);
}
